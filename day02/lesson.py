# Boolean

print(True)
print(False)

# Comparison Operators

print(10 > 5)
print(10 == 10)
print(5 != 3)
print(7 < 2)
print(100 >= 100)

# Logical Operators

print(True and False)
print(True or False)
print(not False)

# if / else

age = 20

if age >= 18:
    print("Access Granted")
else:
    print("Access Denied")